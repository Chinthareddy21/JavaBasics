public class Arithmetic {
	public static void main(String[] args) {
		int a = 25;
		int b = 5;
		/*
			Execution:
				1. System add variables a and b
				2. System gives Output value c
		 */
        //Addition
		int c = a + b;
		System.out.println(c);
		/*
			Execution:
				1. System subtracts variables b from a
				2. System gives Output value d
		 */
        //Subtraction
		int d = a - b;
		System.out.println(d);
		/*
			Execution:
				1. System multiplies variables a with b
				2. System gives Output value e
		 */
        //Multiplication
		int e = a * b;
		System.out.println(e);
		/*
			Execution:
				1. System divides variables a by b
				2. System gives Output value f
		 */
        //Division
		int f = a / b;
		System.out.println(f);
	}
}